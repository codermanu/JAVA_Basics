public class WhileLoop{
    public static void main(String[] args){
        //print integers less than 10
        int i = 0;
        while (i<10){
            System.out.println(i);
            i++;
        }

    //do while method
        int x = 10;
        do{
            System.out.println(x);
            x--;
        }
        while (x>0);
    }
}