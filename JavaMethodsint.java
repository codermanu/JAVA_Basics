public class JavaMethodsint {
    static int add(int x, int y) {
        return x + y;
    }

    static int sub(int x, int y) {
        return x - y;
    }

    public static void main(String args[]) {
        int z = add(9080, 789);
        int a = sub(9080, 789);
        System.out.println(z);
        System.out.println(a);
    }
}