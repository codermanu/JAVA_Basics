public class MathClass{
    public static void main(String[] args){
        //Print the maximum number
        System.out.println(Math.max(3, 9));

        //Print the minimum number
        System.out.println(Math.min(3, 9));

        //Print the Square root of a number
        System.out.println(Math.sqrt(1024));

        //Print the absolute value
        System.out.println(Math.abs(-69));

        //Print a random number between 0(inclusive) and 1(exclusive)
        System.out.println(Math.random());
    }
}